text = "Hello World"

print(text.upper())   # সব অক্ষর বড় করে দিবে (HELLO WORLD)
print(text.lower())   # সব ছোট করে দিবে (hello world)
print(len(text))      # কয়টা অক্ষর আছে দেখাবে
print(text[0])        # প্রথম অক্ষর (H)
print(text[6:])       # ৬ নম্বর থেকে শেষ পর্যন্ত (World)
